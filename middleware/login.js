let status = { userLoggedIn: true};
// --------------------------------------------------------------------------------------------
function login(input, output, next){
	if (status.userLoggedIn == true){
		next();
	}else{
		output.send('User authentification error.');
	}
}
// --------------------------------------------------------------------------------------------
module.exports.userAuthentification = login;
